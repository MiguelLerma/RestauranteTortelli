package restaurantetorteli;

/**
 *
 * @author Alejandro
 */
public class DiasVentas {
    
    private int venta;
    private String mes;
    private String dia;

    public DiasVentas() {
    }

    public int getVenta() {
        return venta;
    }

    public void setVenta(int venta) {
        this.venta = venta;
    }

    public String getMes() {
        return mes;
    }

    public void setMes(String mes) {
        this.mes = mes;
    }

    public String getDia() {
        return dia;
    }

    public void setDia(String dia) {
        this.dia = dia;
    }

    
    
}
