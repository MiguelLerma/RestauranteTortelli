public void llenarTablaProductos(JTable tabla) throws IOException{
    
     
            listaProductosSeleccion.clear();
    
            EntityManagerFactory emf = Persistence.createEntityManagerFactory("RestauranteTorteliPU");//PruebaJPAPU es el nombre de nuestra unidad de persistencia
            
            ProductosFacturaJpaController dao = new ProductosFacturaJpaController(emf);//Creamos un controlador de personal
           
                    
            ProductosFactura productos = new ProductosFactura();
            
            List<ProductosFactura> listaProductos = dao.findProductosFacturaEntities();
            
            
            
            for (int i = 0; i < listaProductos.size(); i++) {
                
                
                if(listaProductos.get(i).getProductosFacturaPK().getFacturaFacturaid() == idFactura){
                    
                    
                    listaProductosSeleccion.add(listaProductos.get(i));
                    
                }
        
    }
           
            
            
         
    
    
    
    
           Object listaItems[][]  = new Object[listaProductosSeleccion.size()][3];
          
          
      
          
          
           int precioAcum = 0;
          
        for (int i = 0; i < listaProductosSeleccion.size(); i++) {
            
             
            listaItems[i][0] = listaProductosSeleccion.get(i).getNombreProducto();
            listaItems[i][1] = listaProductosSeleccion.get(i).getPrecioProducto();
            listaItems[i][2] = listaProductosSeleccion.get(i).getCantidad();

            
        }
        
       
        String columna[] = new String[] {"Nombre", "Precio","Cantidad"};
        emf.close();
        
        tabla.setDefaultRenderer(Object.class,new RenderTabla());
        
        
        
        DefaultTableModel modelo = new DefaultTableModel(listaItems, columna);
        
       
        tabla.setModel(modelo);


    
}




public void llenarDatos(){
    
    
    
      EntityManagerFactory emf = Persistence.createEntityManagerFactory("RestauranteTorteliPU");//PruebaJPAPU es el nombre de nuestra unidad de persistencia
            
      FacturaJpaController dao = new FacturaJpaController(emf);//Creamos un controlador de personal
           
      Factura factura = new Factura();
      
      factura = dao.findFactura(idFactura);
          
    TFcedula.setText(Integer.toString(factura.getCedulacliente()));
    TFiva.setText(Integer.toString(factura.getIva()));
    TFtotal.setText(Integer.toString(factura.getTotalfactura()));
    TFfecha.setText(""+factura.getFechafactura().getMonth()+"/"+factura.getFechafactura().getDay());

    
    
}

